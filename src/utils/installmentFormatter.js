
export function InstallmentNumber(num){
	if(num===1){
		return 'First Payment'
	}
	if(num===2){
		return 'Second Payment'
	}

	if(num===3){
		return 'Third Payment'
	}

	if(num===4){
		return 'Fourth Payment'
	}
    if(num===5){
		return 'Fifth Payment'
	}
	if(num===6){
		return 'Sixth Payment'
	}
	if(num===7){
		return 'Seventh Payment'
	}

	if(num===8){
		return 'Eigth Payment'
	}
	if(num===9){
		return 'Ninth Payment'
	}
	if(num===1){
		return 'Tenth Payment'
	}
}